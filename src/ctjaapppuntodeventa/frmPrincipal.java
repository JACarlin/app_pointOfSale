    private void btnquitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnquitarActionPerformed
        DefaultTableModel model=(DefaultTableModel)tabla.getModel();
        int s= tabla.getSelectedRow();
        if(s!=-1){
        int cant=Negocio.NegocioProducto.pedido.get(s).getCantidadProducto();
        proceso.modificarcantidadr(Negocio.NegocioProducto.pedido.get(s), cant);
        double resta=Double.parseDouble(tabla.getModel().getValueAt(s, 3).toString());
        subtotal=subtotal-resta;
        JOptionPane.showMessageDialog(null, resta);
        model.removeRow(s);
        total= subtotal+ (subtotal*0.16);
        Negocio.NegocioProducto.pedido.remove(s);
        txtsubtotal.setText("Sub-Total: $"+subtotal);
        txttotal.setText("Total: $"+total);
        }
    }//GEN-LAST:event_btnquitarActionPerformed

