    public void traerDatos(int c, Producto p){
        DefaultTableModel model=(DefaultTableModel)tabla.getModel();
                for (int i = 0; i < tabla.getRowCount(); i++) {
                model.removeRow(i);
                i-=1;
        }

        Negocio.NegocioProducto.pedido.add(p);
        proceso.modificarcantidad2(p, c);
        temp=null;
        double precio;
        //model=null;
        for(int i=0;i<Negocio.NegocioProducto.pedido.size();i++){
        temp=Negocio.NegocioProducto.pedido.get(i);
        if(temp.getCantidadProducto()<5){
        precio=temp.getPrecioUnitario();
        }else{
            precio=temp.getPrecioMayoreo();
        }
        model.addRow(new Object[]{
            
           temp.getNombreProducto(),temp.getCantidadProducto(),precio,(temp.getCantidadProducto()*precio)
        });
        }
        tabla.setModel(model);
        subtotal=0;
        total=0;
        for(int i=0;i<tabla.getRowCount();i++){
            subtotal=subtotal+(Double.parseDouble(tabla.getModel().getValueAt(i, 3).toString()));
        }
        total= subtotal+ (subtotal*0.16);
        txtsubtotal.setText("Sub-Total: $"+subtotal);
        txttotal.setText("Total: $"+total);
    }
