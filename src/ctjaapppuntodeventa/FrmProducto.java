    private void consultarproductos(){
        if(Negocio.NegocioProducto.lstProductos.size()>0){
            
            DefaultTableModel model=(DefaultTableModel)tabla.getModel();
            for(int i=0;i<Negocio.NegocioProducto.lstProductos.size();i++){
                Producto temp= lstProductos.get(i);
                JLabel imgLabel  =  new JLabel();
        Image img = new ImageIcon(temp.getImagen()).getImage();
        Image newimg = img.getScaledInstance(150, 150,  java.awt.Image.SCALE_SMOOTH);
        ImageIcon imageIcon = new ImageIcon(newimg);
        imgLabel.setIcon(imageIcon);
        tabla.getColumn("Imagen").setCellRenderer(new LabelRendar());
        model.addRow(new Object[]{
            //imgFoto.getIcon(),
            imgLabel,
            temp.getNombreProducto(),
            temp.getCantidadProducto(),
            temp.getPrecioUnitario(),temp.getPrecioMayoreo()
        });
        updateRowHeights();
            }
        tabla.setModel(model);
        }
    }
