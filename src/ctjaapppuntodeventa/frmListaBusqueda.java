    private void llenartabla(){
        if(temp!=null){
            DefaultTableModel model=(DefaultTableModel)tabla.getModel();
        model.addRow(new Object[]{
           temp.getNombreProducto(),temp.getDescripcionProducto(),temp.getPrecioUnitario(),temp.getCantidadProducto()
        });
        tabla.setModel(model);
           escalarImagen(temp.getImagen()   );
        }else{
            JOptionPane.showMessageDialog(null,
                "No se encontro el producto");
           
        }
        }
    private void escalarImagen(String urlFile){
      
        JLabel imgLabel  =  new JLabel();
        Image img = new ImageIcon(urlFile).getImage();
        Image newimg = img.getScaledInstance(150, 150,  java.awt.Image.SCALE_SMOOTH);
        ImageIcon imageIcon = new ImageIcon(newimg);
        imgFoto.setIcon(imageIcon);



   }
